package com.workflow.politicas.dto;

public class KpiActivityMetricDto {
    private String workflowActivityId;
    private String activityName;
    private String policyId;
    private String policyName;
    private long pendingCount;
    private long inProgressCount;
    private long completedCount;
    private long overdueCount;
    private String averageDuration;
    private String averageActiveWait;

    public String getWorkflowActivityId() {
        return workflowActivityId;
    }

    public void setWorkflowActivityId(String workflowActivityId) {
        this.workflowActivityId = workflowActivityId;
    }

    public String getActivityName() {
        return activityName;
    }

    public void setActivityName(String activityName) {
        this.activityName = activityName;
    }

    public String getPolicyId() {
        return policyId;
    }

    public void setPolicyId(String policyId) {
        this.policyId = policyId;
    }

    public String getPolicyName() {
        return policyName;
    }

    public void setPolicyName(String policyName) {
        this.policyName = policyName;
    }

    public long getPendingCount() {
        return pendingCount;
    }

    public void setPendingCount(long pendingCount) {
        this.pendingCount = pendingCount;
    }

    public long getInProgressCount() {
        return inProgressCount;
    }

    public void setInProgressCount(long inProgressCount) {
        this.inProgressCount = inProgressCount;
    }

    public long getCompletedCount() {
        return completedCount;
    }

    public void setCompletedCount(long completedCount) {
        this.completedCount = completedCount;
    }

    public long getOverdueCount() {
        return overdueCount;
    }

    public void setOverdueCount(long overdueCount) {
        this.overdueCount = overdueCount;
    }

    public String getAverageDuration() {
        return averageDuration;
    }

    public void setAverageDuration(String averageDuration) {
        this.averageDuration = averageDuration;
    }

    public String getAverageActiveWait() {
        return averageActiveWait;
    }

    public void setAverageActiveWait(String averageActiveWait) {
        this.averageActiveWait = averageActiveWait;
    }
}
